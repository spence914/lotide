const letterPositions = function(sentence) {
  const results = {};
  // Use a for..of loop to loop through the given sentence, writing to the result object
  // a key for each letter, then an array as the value for each key, inside of which is the
  // index where the letter is found

  for (let i = 0; i < sentence.length; i++) {
    if (sentence[i] !== " ") {
      if (results[sentence[i]]) {
        results[sentence[i]].push(i);
      } else {
        results[sentence[i]] = [i];
      }
    }
  }
  return results;
};

assertArraysEqual(letterPositions("hello").e, [1]);
assertArraysEqual(letterPositions("hello").l, [2, 3]);
assertArraysEqual(letterPositions("lighthouse labs").l, [0, 11]);

